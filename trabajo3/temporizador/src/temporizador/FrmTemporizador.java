    private void btnStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStopActionPerformed
        temporizador.stop();
        if (contador == 0)
        {
            acierto+=1;
            lblAciertos.setText(String.valueOf(acierto));
            intentos+=1;
            lblTotal.setText(String.valueOf(intentos));
        }
        else 
        {
            intentos+=1;
            lblTotal.setText(String.valueOf(intentos));
        }
    }//GEN-LAST:event_btnStopActionPerformed

