    public FrmTemporizador() {
        this.temporizador = new Timer(100, new ActionListener()
        {
            public void actionPerformed(ActionEvent e)
            {
                //codigo oara ejecutar
                contador+=1;
                //ahora puedo editar el calor del contador de las decimas
                //uso el String.valueOf para cambiar a string el valor de contador
                lblDecimas.setText(String.valueOf(contador));
                
                if (contador == 9)
                    {
                        contador=0;
                        if (contador == 0)
                            {
                               segundo+=1;
                               lblSegundos.setText(String.valueOf(segundo));
                            }
                    }
                
                if (segundo == 59)
                    {
                        segundo=0;
                        if (segundo == 0)
                            {
                                minuto+=1;
                                lblMinutos.setText(String.valueOf(minuto));
                            }
                    }
                if (segundo < 10)
                {
                    lblSegundos.setText("0" + String.valueOf(segundo));
                }
                if (minuto < 10)
                {
                    lblMinutos.setText("0" + String.valueOf(minuto));
                }
                if (contador < 10)
                {
                    lblDecimas.setText("0" + String.valueOf(contador));
                }
            }
        });
        initComponents();
    }

